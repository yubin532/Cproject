//2) main은 함수의 이름입니다.
//	 함수는 프로그램을 구성하는 구성 요소, 단위 명령문 집합을 의미합니다.
//	 C언어에서 main 함수는 프로젝트에서 기능을 작동시키기 위한 함수입니다.
//	 함수의 () 부분은 해당 함수를 실행할 때 전달해줄 값을 적는 위치입니다. 현재는 따로 작성되지 않았습니다.
